/// Canonical button identifiers for a standard gamepad.
///
/// All buttons are digital (pressed or not). Mapped by physical position
/// using the Xbox layout convention.
enum GamepadButton {
  a,
  b,
  x,
  y,
  leftBumper,
  rightBumper,
  leftStick,
  rightStick,
  dpadUp,
  dpadDown,
  dpadLeft,
  dpadRight,
  start,
  select,
  guide,
}
